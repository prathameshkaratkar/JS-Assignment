/*
    Define a funcgion toUpperCaseWord that takes a string and returns 
    it in uppercase
*/

// Using normal function declaration syntax

function toUpperCaseWord(word) {
    return word.toUpperCase();
}

console.log(toUpperCaseWord('prathamesh'))


//Using arrow function syntax


const toUpperCaseWordArrow = (word) => {
    return word.toUpperCase();
}

console.log(toUpperCaseWordArrow('function'))