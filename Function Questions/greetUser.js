// Using normal function declaration syntax.

function greetUser(name) {
    console.log(`Hello, ${name}`)
}

greetUser('prathamesh')


// Using arrow function syntax.

const greetUserArrow = (name) => {
    console.log(`Hello, ${name}`)
}

greetUserArrow('prathamesh karatkar')